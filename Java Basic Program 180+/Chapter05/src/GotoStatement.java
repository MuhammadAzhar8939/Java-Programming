/*
 * In Java, the goto statement is not used as it is considered a harmful and unnecessary control flow construct.
 *  Instead, loops like for, while, or do-while are used to repeat a block of code multiple times.
 */

public class GotoStatement {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println("Hello Java");
        }
        System.out.println("End Of Program ...");

    }
}
