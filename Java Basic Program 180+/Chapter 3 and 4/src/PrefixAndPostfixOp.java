public class PrefixAndPostfixOp {
    public static void main(String[] args) {
        int a=0;
        System.out.println("Value of a : "+ a);
        System.out.println("Value of a++ : "+ a++);//keep it same in this step but increase by 1 for next step
        System.out.println("Value of ++a : "+ ++a);//increase by 1 in this step also
        System.out.println("Value of a-- : "+ a--);
        System.out.println("Value of --a : "+ --a);
    }
}
